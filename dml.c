/* ===================== STAGE 1: single character (unchanged) ===================== */
void display_char(unsigned char ch,unsigned int delay)
{
	unsigned int j=0,l;
	for(l=0;l<delay;l++)
	{
		for(j=0;j<8;j++)
		{
			write_row_data(0x00);
			SIPO_74LS164_disp(font[ch-32][j],1);
			write_row_data(1<<j);
			delay_us(40);
		}
		write_row_data(0x00);
	}
}

/* ===================== STAGE 2: 4 fixed characters ===================== */
/* Displays one character on each of the 4 matrices simultaneously.
   Call this repeatedly in a loop for a short visible duration, since
   each call only draws ONE full 8-row refresh pass - persistence of
   vision needs many repeated passes per second to look solid. */
void display_4chars_once(unsigned char c1,unsigned char c2,unsigned char c3,unsigned char c4)
{
	unsigned int j;
	for(j=0;j<8;j++)
	{
		write_row_data(0x00);
		SIPO_74LS164_disp(font[c1-32][j],1);
		SIPO_74LS164_disp(font[c2-32][j],2);
		SIPO_74LS164_disp(font[c3-32][j],3);
		SIPO_74LS164_disp(font[c4-32][j],4);
		write_row_data(1<<j);
		delay_us(40);
	}
	write_row_data(0x00);
}

void display_scroll(unsigned char *c1)
{
	unsigned int j,i;
	for(i=0;i<150;i++)
	{
	for(j=0;j<8;j++)
	{
		//write_row_data(0x00);
		WRITEBYTE(IOPIN1,23,0);
		SIPO_74LS164_disp(font[c1[0]-32][j],1);
		SIPO_74LS164_disp(font[c1[1]-32][j],2);
		SIPO_74LS164_disp(font[c1[2]-32][j],3);
		SIPO_74LS164_disp(font[c1[3]-32][j],4);
		//write_row_data(1<<j);
		WRITEBYTE(IOPIN1,23,(1<<j));
		delay_us(40);
	}
}
	write_row_data(0x00);
}

void display_4chars(unsigned char c1,unsigned char c2,unsigned char c3,unsigned char c4,unsigned int duration_ms)
{
	unsigned int t;
	/* roughly 1 refresh pass = 8*40us = 320us; loop enough passes
	   to cover duration_ms (approx, good enough for visibility) */
	for(t=0; t<duration_ms*3; t++)
		display_4chars_once(c1,c2,c3,c4);
}

/* ===================== STAGE 3: Scrolling text ===================== */
/* The font is stored ROW-major: font[ch][row] = one byte, each bit =
   one column in that row. To scroll smoothly, for every scroll position
   we recompute the 32 visible columns (4 displays x 8 cols) bit-by-bit
   by pulling from whichever character(s) currently sit under that
   window. This avoids needing a transposed/column-major font table. */

#define MAX_SCROLL_CHARS 80

static unsigned char get_col_bit(char *msg, unsigned int msgLen, unsigned int col, unsigned int row)
{
	unsigned int charIndex = col/8;
	unsigned int bitIndex  = col%8;
	if(charIndex >= msgLen)
		return 1; /* blank padding column = bit off (this font uses 1=off,0=on) */
	return (font[msg[charIndex]-32][row] >> (7-bitIndex)) & 1;
}

